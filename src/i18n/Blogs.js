function getArabicBlogs() {
  return blogs?.reduce((acc, blog) => {
    const newBlog = {
      id: blog.id,
      name: blog.name_ar,
      title: blog?.title_ar,
      description: blog?.description_ar,
      properties: blog?.properties?.map((prop) => {
        const newProperties = {
          title: prop?.title_ar,
          description: prop?.description_ar,
          items: prop?.items?.map((item) => {
            return {
              title: item?.title_ar,
              description: item?.description_ar,
              values: item?.values?.map((val) => {
                const newValue = {
                  value: val?.value_ar,
                };
                return newValue;
              }),
            };
          }),
        };
        return newProperties;
      }),
    };
    acc[blog.id] = newBlog;
    return acc;
  }, {});
}
